package controls;

public class Morning0225 {
	public static void main(String[] args) {
		int sum = 0;
		
		for(int i =11; i<=30; i++) {
			sum += i;
				
		System.out.println("11~30 합: "+ sum);
		}
	}
}

	//11부터 30까지의 수의 합을 구하도록 for 반복문.
	//11과 30을 변수로 입력받아서 합을 구하는 for반복문.
	//11과 30을 매개변수로 받아서 합을 구하는 메소드.